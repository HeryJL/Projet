public class DataPoint {
    private String x;
    private int y;

    public DataPoint(String x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getters et Setters

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }


    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
